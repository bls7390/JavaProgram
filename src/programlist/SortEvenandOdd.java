public class SortEvenandOdd {

	public static void main(String[] args) {
		int []a= {1,5,6,4,7,12,13};
		
		for(int i=0; i<a.length; i++)
		{
				if(a[i]%2==0)
				{
					System.out.println(a[i]);
							
					
				}
			}
		for(int i=0; i<a.length; i++)
		{
				if(a[i]%2!=0)
				{
					System.out.println(a[i]);
							
					
				}
			}
		
		}

	}


